                group as the Talented Tenth and believed that African Americans needed the chances for advanced education to develop its leadership.
                Racism was the main target of Du Bois's polemics, and he strongly protested against lynching, Jim Crow laws, and discrimination in
                education and employment. His cause included people of color everywhere, particularly Africans and Asians in colonies. He was a proponent
                of Pan-Africanism and helped organize several Pan-African Congresses to fight for the independence of African colonies from European powers.
                Du Bois made several trips to Europe, Africa and Asia. After World War I, he surveyed the experiences of American black soldiers in France
                and documented widespread bigotry in the United States military.
                Du Bois was a prolific author. His collection of essays, The Souls of Black Folk, was a seminal work in African-American 
                literature; and his 1935 magnum opus Black Reconstruction in America challenged the prevailing orthodoxy that blacks were
                responsible for the failures of the Reconstruction Era. He wrote one of the first scientific treatises in the field of 
                American sociology, and he published three autobiographies, each of which contains insightful essays on sociology, politics
                and history. In his role as editor of the NAACP's journal The Crisis, he published many influential pieces. Du Bois believed
                that capitalism was a primary cause of racism, and he was generally sympathetic to socialist causes throughout his life. 
                He was an ardent peace activist and advocated nuclear disarmament. The United States' Civil Rights Act, embodying many of the 
                reforms for which Du Bois had campaigned his entire life, was enacted a year after his death.
