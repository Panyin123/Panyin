				one of the co-founders of the National Association for the Advancement of Colored People (NAACP) in 1909.
                Du Bois rose to national prominence as the leader of the Niagara Movement, a group of African-American activists 
                who wanted equal rights for blacks. Du Bois and his supporters opposed the Atlanta compromise, an agreement crafted by Booker T.
                Washington which provided that Southern blacks would work and submit to white political rule, while Southern whites guaranteed 
